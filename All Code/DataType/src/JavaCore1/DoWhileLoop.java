package JavaCore1;


public class DoWhileLoop {
    
    public static void main(String[] args) {
        
        System.out.println("Even numbers are : ");
        
        int i=2;
        do{
            
            System.out.println(i);
            i+=2;
            
        }while(i<=100);
        
        System.out.println("\nOdd numbers are : ");
        
        int j=1;
        do{
            
            System.out.println(j);
            j+=2;
            
        }while(j<=100);
        
    }
    
}
