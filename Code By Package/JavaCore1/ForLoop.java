package JavaCore1;


public class ForLoop {
    
    public static void main(String[] args) {
        
        System.out.println("Even numbers are : ");
        
        for (int i=2; i<=100; i+=2){            
            System.out.println(i);
        }
        
        System.out.println("\n\nOdd numbers are : ");
        
        for (int i=99; i>=1; i-=2){            
            System.out.println(i);
        }
        
    }
    
}
