package JavaCore1;

import java.util.Scanner;

public class UnaryOperator {
    
    public static void main(String[] args) {
        
        int x = 10, y = -10, result;
        
        result = +x;        
        System.out.println("Result = "+result);
        
        result = -x;        
        System.out.println("Result = "+result);
        
        result = +y;        
        System.out.println("Result = "+result);
        
        result = -y;        
        System.out.println("Result = "+result);
    }
}
