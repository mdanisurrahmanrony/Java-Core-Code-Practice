package JavaCore1;

public class WhileLoop {
    
    public static void main(String[] args) {
        
        int i=2;
        
        System.out.println("Even numbers are : ");
        
        while(i<=100){
            
            System.out.println(i);
            i+=2;
            
        }
        
        int j=1;
        
        System.out.println("\nOdd numbers are : ");
        
        while(j<=99){
            
            System.out.println(j);
            j+=2;
            
        }
        
    }
    
}
