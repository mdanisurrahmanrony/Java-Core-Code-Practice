package JavaCore1;

public class whileVSdoWhileLopp {
    
    public static void main(String[] args) {
        
        int x = 6;  //Condition prothomei false hole ekbaro kaj korbe na
        while(x<=5){
            System.out.println(x+" Bangladesh");
            x++;
        }
        
        int y = 6;  //Condition prothomei false holeo ekbar ontoto kaj korbe
        do{
            System.out.println(y+" Bangladesh");
            y++;
        }while(y<=5);
        
    }
    
}
