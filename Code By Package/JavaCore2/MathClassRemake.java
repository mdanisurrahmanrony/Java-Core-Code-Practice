package JavaCore2;


public class MathClassRemake {
    
    public static void main(String[] args) {
        
        int x = Math.abs(-9);
        System.out.println(x);
        
        double y = Math.abs(-9.5);
        System.out.println(y);
        
        System.out.println(Math.abs(-25.9));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.pow(2, 3));
        System.out.println(Math.PI);
        System.out.println(Math.log(2.0));
        System.out.println(Math.exp(1.0));
        System.out.println(Math.max(-2.3, 12.8));
        System.out.println(Math.min(-2.3, 12.8));
        System.out.println(Math.ceil(5.6));       //doshomiker uporer diker number ke count korbe
        System.out.println(Math.floor(5.6));       //doshomiker nicher diker number ke count korbe
        
    }
    
}
